/*@ngInject*/
export default function EntityGroupColumnDialogController($scope, $mdDialog, $filter, $q, types, utils,
                                                          readOnly, column, entityType, columnTypes, entityField) {

    var vm = this;

    vm.types = types;
    vm.readOnly = readOnly;
    vm.column = column;
    vm.entityType = entityType;
    vm.columnTypes = columnTypes;
    vm.entityField = entityField;
    vm.sortOrder = types.entityGroup.sortOrder;

    vm.cancel = cancel;
    vm.save = save;

    function cancel() {
        $mdDialog.cancel();
    }

    function save() {
        $scope.theForm.$setPristine();
        $mdDialog.hide(vm.column);
    }
}
